                <h1>The "Women's Doubles" Sector</h1>
                <p style="text-align: justify;">
                Sejarah sektor Women's Doubles (ganda putri) dalam olahraga bulutangkis telah melahirkan rivalitas dan pencapaian luar biasa sepanjang waktu. Dimulai pada tahun 1957 dengan gelaran All England Championships sebagai kejuaraan ganda putri pertama, para pemain wanita telah menunjukkan keterampilan dan kekompakan yang mengagumkan. 
                Dari tahun 1992 hingga 2012, pasangan ganda putri asal Korea, Gil Young-ah dan Jang Hye-ock, menguasai dominasi dengan memenangkan lima gelar juara dunia. Namun, dominasi Korea ini kemudian tergeser oleh pasangan China, seperti Gao Ling dan Huang Sui, yang memenangkan medali emas Olimpiade Sydney 2000. 
                Selain itu, pasangan Tiongkok, seperti Tian Qing dan Zhao Yunlei, terkenal dengan kekuatan dan kerjasama mereka, memenangkan gelar-gelar bergengsi, termasuk Kejuaraan Dunia dan Olimpiade. 
