                <h1>Sejarah Badminton</h1>
                <p style="text-align: justify;">
                Badminton memiliki sejarah yang panjang dan bermula dari permainan tradisional di India kuno yang disebut Poona. Pada abad ke-19, permainan ini diperkenalkan ke Inggris oleh petugas tentara Inggris. Di Inggris, peraturan resmi untuk badminton ditetapkan oleh klub Bath Badminton pada tahun 1877, dan nama badminton mulai digunakan pada tahun 1887. 
                Permainan ini semakin populer di Inggris dan negara-negara Eropa, dan pada tahun 1934, International Badminton Federation (IBF) didirikan untuk mengkoordinasikan olahraga ini secara internasional. Badminton menjadi olahraga yang diakui secara internasional dan masuk dalam program Olimpiade pada tahun 1992. 
                Saat ini, badminton terus berkembang dan menjadi salah satu olahraga yang paling populer di dunia, dengan turnamen-turamen bergengsi seperti All England Championships dan Kejuaraan Dunia yang menarik minat penggemar di seluruh dunia.  
                </p>
